package co.com.ceiba.estacionamiento.util;

public class FechaDiasHoras {
	
	private long dias;
	private long  horas;
	
	public FechaDiasHoras(long dias, long horas) {
		super();
		this.dias = dias;
		this.horas = horas;
	}

	public long getDias() {
		return dias;
	}

	public void setDias(long dias) {
		this.dias = dias;
	}

	public long getHoras() {
		return horas;
	}

	public void setHoras(long horas) {
		this.horas = horas;
	}
	

	
}
